"""Generate the FPGA character ROM.

The FPGA firmware writes ASCII-like bytes today, so this builds a hybrid:
printable ASCII remains readable, low Commodore screen-code letters are added,
and the upper punctuation/lowercase area carries PETSCII-style block graphics.
The display path treats bit 7 as reverse video.
"""
from __future__ import annotations

import re
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "rtl" / "mem" / "char_rom.vhd"


def rows(*values: int) -> list[int]:
    if len(values) != 8:
        raise ValueError("glyphs must contain exactly 8 rows")
    return list(values)


def parse_existing(path: Path) -> list[list[int]]:
    text = path.read_text(encoding="utf-8")
    values = [int(m, 16) for m in re.findall(r'x"([0-9A-Fa-f]{2})"', text)]
    if len(values) < 1024:
        raise ValueError(f"expected at least 1024 ROM bytes in {path}, found {len(values)}")
    return [values[i : i + 8] for i in range(0, 1024, 8)]


def build_rom(base: list[list[int]]) -> list[list[int]]:
    rom = [glyph[:] for glyph in base[:128]]

    # Commodore screen-code compatible letters/symbols in the control range.
    rom[0x00] = base[0x40][:]  # @
    for i in range(26):
        rom[0x01 + i] = base[0x41 + i][:]
    rom[0x1B] = base[0x5B][:]
    rom[0x1C] = rows(0x1C, 0x36, 0x30, 0x7C, 0x30, 0x30, 0x7E, 0x00)  # pound
    rom[0x1D] = base[0x5D][:]
    rom[0x1E] = rows(0x18, 0x3C, 0x7E, 0x18, 0x18, 0x18, 0x18, 0x00)  # up arrow
    rom[0x1F] = rows(0x00, 0x10, 0x30, 0x7F, 0x7F, 0x30, 0x10, 0x00)  # left arrow

    # PETSCII-style block and line graphics. These occupy the area that was
    # lowercase ASCII in the old ROM; ASCII uppercase, digits, and punctuation
    # remain unchanged for firmware/BASIC output.
    graphics = {
        0x60: rows(0x00, 0x00, 0x00, 0xFF, 0xFF, 0x00, 0x00, 0x00),  # hline
        0x61: rows(0x18, 0x18, 0x18, 0x18, 0x18, 0x18, 0x18, 0x18),  # vline
        0x62: rows(0x18, 0x18, 0x18, 0xFF, 0xFF, 0x18, 0x18, 0x18),  # cross
        0x63: rows(0x00, 0x00, 0x00, 0x1F, 0x1F, 0x18, 0x18, 0x18),  # ul joint
        0x64: rows(0x00, 0x00, 0x00, 0xF8, 0xF8, 0x18, 0x18, 0x18),  # ur joint
        0x65: rows(0x18, 0x18, 0x18, 0x1F, 0x1F, 0x00, 0x00, 0x00),  # ll joint
        0x66: rows(0x18, 0x18, 0x18, 0xF8, 0xF8, 0x00, 0x00, 0x00),  # lr joint
        0x67: rows(0x00, 0x00, 0x00, 0xFF, 0xFF, 0x18, 0x18, 0x18),  # tee down
        0x68: rows(0x18, 0x18, 0x18, 0xFF, 0xFF, 0x00, 0x00, 0x00),  # tee up
        0x69: rows(0x18, 0x18, 0x18, 0x1F, 0x1F, 0x18, 0x18, 0x18),  # tee right
        0x6A: rows(0x18, 0x18, 0x18, 0xF8, 0xF8, 0x18, 0x18, 0x18),  # tee left
        0x6B: rows(0x80, 0xC0, 0xE0, 0xF0, 0x78, 0x3C, 0x1E, 0x0F),  # diagonal
        0x6C: rows(0x01, 0x03, 0x07, 0x0F, 0x1E, 0x3C, 0x78, 0xF0),  # diagonal
        0x6D: rows(0xAA, 0x55, 0xAA, 0x55, 0xAA, 0x55, 0xAA, 0x55),  # checker
        0x6E: rows(0xCC, 0xCC, 0x33, 0x33, 0xCC, 0xCC, 0x33, 0x33),  # checker
        0x6F: rows(0xF0, 0xF0, 0xF0, 0xF0, 0x0F, 0x0F, 0x0F, 0x0F),  # split
        0x70: rows(0xF0, 0xF0, 0xF0, 0xF0, 0xF0, 0xF0, 0xF0, 0xF0),  # left half
        0x71: rows(0x0F, 0x0F, 0x0F, 0x0F, 0x0F, 0x0F, 0x0F, 0x0F),  # right half
        0x72: rows(0xFF, 0xFF, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x00),  # top half
        0x73: rows(0x00, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0xFF, 0xFF),  # bottom half
        0x74: rows(0xF0, 0xF0, 0xF0, 0xF0, 0x00, 0x00, 0x00, 0x00),  # quadrant
        0x75: rows(0x0F, 0x0F, 0x0F, 0x0F, 0x00, 0x00, 0x00, 0x00),
        0x76: rows(0x00, 0x00, 0x00, 0x00, 0xF0, 0xF0, 0xF0, 0xF0),
        0x77: rows(0x00, 0x00, 0x00, 0x00, 0x0F, 0x0F, 0x0F, 0x0F),
        0x78: rows(0x81, 0x42, 0x24, 0x18, 0x18, 0x24, 0x42, 0x81),  # X
        0x79: rows(0x18, 0x3C, 0x7E, 0xFF, 0xFF, 0x7E, 0x3C, 0x18),  # diamond
        0x7A: rows(0x18, 0x3C, 0x7E, 0xDB, 0xFF, 0x24, 0x5A, 0xA5),  # ball
        0x7B: rows(0x00, 0x3C, 0x7E, 0x7E, 0x7E, 0x7E, 0x3C, 0x00),  # disk
        0x7C: rows(0x18, 0x3C, 0x7E, 0x18, 0x18, 0x7E, 0x3C, 0x18),  # arrows
        0x7D: rows(0x18, 0x18, 0x18, 0xFF, 0xFF, 0x18, 0x18, 0x18),  # plus line
        0x7E: rows(0x76, 0xDC, 0x00, 0x76, 0xDC, 0x00, 0x00, 0x00),  # tilde waves
        0x7F: rows(0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF),  # full block
    }
    for code, glyph in graphics.items():
        rom[code] = glyph

    return rom


def render(rom: list[list[int]]) -> str:
    lines = [
        "-- Character ROM: ASCII-compatible PETSCII-style 8x8 pixel patterns",
        "-- Addressing: addr = char_code(6:0) & pixel_row(2:0)",
        "-- Output: bit 7 = leftmost pixel. The VGA path uses char_code(7) as reverse video.",
        "library ieee;",
        "use ieee.std_logic_1164.all;",
        "use ieee.numeric_std.all;",
        "",
        "use work.sbc_pkg.all;",
        "",
        "entity char_rom is",
        "  port (",
        "    addr : in  std_logic_vector(9 downto 0);",
        "    dout : out data_t",
        "  );",
        "end entity;",
        "",
        "architecture rtl of char_rom is",
        "  type rom_t is array (0 to 1023) of data_t;",
        "",
        "  constant rom : rom_t := (",
    ]

    names = {
        0x00: "PETSCII @ / screen-code 0",
        0x01: "PETSCII A-Z screen-code block",
        0x20: "ASCII/PETSCII punctuation, digits, uppercase letters",
        0x60: "PETSCII-style block and line graphics",
    }
    for code, glyph in enumerate(rom):
        label = names.get(code, f"Character 0x{code:02X}")
        lines.append(f"    -- {label}")
        values = [f'x"{b:02X}"' for b in glyph]
        suffix = "," if code != 0x7F else ""
        lines.append("    " + ", ".join(values) + suffix)

    lines.extend(
        [
            "  );",
            "",
            "begin",
            "  dout <= rom(to_integer(unsigned(addr)));",
            "end architecture;",
            "",
        ]
    )
    return "\n".join(lines)


def main() -> None:
    base = parse_existing(OUT)
    OUT.write_text(render(build_rom(base)), encoding="utf-8")


if __name__ == "__main__":
    main()
